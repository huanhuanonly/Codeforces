#include <bits/stdc++.h>

using int8 = signed char;
using int16 = short;
using int32 = int;
using int64 = long long;

using uint8 = unsigned char;
using uint16 = unsigned short;
using uint32 = unsigned int;
using uint64 = unsigned long long;

#ifdef __GNUC__
using int128 = __int128;
using uint128 = unsigned __int128;
#endif

using real32 = float;
using real64 = double;
using real128 = long double;

int main(void)
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr), std::cout.tie(nullptr);

#ifdef __huanhuanonly_localhost
    ::freopen("in", "r", stdin);
    ::freopen("out", "w", stdout);
#endif

    int _;
    std::cin >> _;

    while (_--)
    {
        int n;
        std::cin >> n;

        std::list<std::deque<int>> v;

        for (int i = 0, len; i < n; ++i)
        {
            std::cin >> len;
            v.emplace_back();

            for (int j = 0, val; j < len; ++j)
            {
                std::cin >> val;
                v.back().push_back(val);
            }
        }

        v.sort();

        std::vector<int> ans;

        while (not v.empty())
        {
            const int len = v.front().size();

            ans.insert(ans.end(), v.front().begin(), v.front().end());
            v.pop_front();

            for (auto it = v.begin(); it != v.end(); )
            {
                if (it->size() > len)
                {
                    it->erase(it->begin(), it->begin() + len);
                    ++it;
                }
                else
                {
                    it = v.erase(it);
                }
            }

            v.sort();
        }

        for (int i : ans)
        {
            std::cout << i << ' ';
        }

        std::cout.put('\n');
    }

    return 0;
}